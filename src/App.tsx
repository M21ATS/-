                  className="w-full border-2 lg:border-4 border-[#1A1A1A] rounded-xl lg:rounded-2xl px-3 lg:px-6 py-2 lg:py-4 outline-none focus:bg-gray-50 transition-all font-bold text-sm lg:text-lg"
